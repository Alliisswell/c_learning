#include<iostream>

using namespace std;

//语法：返回值类型 函数名(数据类型) {}

//在现阶段函数的占位参数存在意义不大，我们还用不到，但是后面的课程中会用到该技术

void func21(int a, int) {
	cout << "this is func21" << endl;
}

void func22(int a, int = 10) {
	cout << "this is func22" << endl;
}
int main2() {

	func21(10, 10);	//C++中函数的形参列表里可以有占位参数，用来做占位，调用函数时必须填补该位置

	func22(10);	//占位参数还可以有默认值，并且当占位参数有了默认值时，调用函数时可以不用填补该位置

	system("pause");
	return 0;
}